/* Change LED colors depending on the layer. */
layer_state_t layer_state_set_user(layer_state_t state) {
    switch (get_highest_layer(state)) {
    case _QWERTY:
        rgblight_setrgb (0x66, 0x00, 0x33); /* Dark red */
        break;
    case _ARROWS:
        rgblight_setrgb (0xFF, 0xFF, 0xFF); // White
       // rgblight_setrgb (0xFF,  0x00, 0x00); /* red */
        break;
    case _SYMBOL:
        rgblight_setrgb (0x00,  0xFF, 0x00); // Green
        break;
    case _FUNC:
        rgblight_setrgb (0x00, 0x00, 0xFF);  // Blue
        break;
    case _MOUSE:
        rgblight_setrgb (0xFF,  0xA6, 0x4D); // Orange
        break;
    case _NUMPAD:
        rgblight_setrgb (0xB0,  0x00, 0xDC); // Purple b000dc
        break;
    default: //  for any other layers, or the default layer
        rgblight_setrgb (0x66, 0x00, 0x33); /* Dark red */
        break;
    }
  return state;
};
